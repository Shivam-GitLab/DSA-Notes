public class Relational {
    public static void main(String[] args) {
        /*
         * # Relational Operator
         * ==  - Is Equal To
         * !=  - Not Equal To
         * >   - Greater Than
         * >=  - Greater Than Equal To
         * <   - Leass Than
         * <=  - Leass Than Equal To
         * 
         */

        int a = 20;
        int b = 30;
        System.out.println("Relational Operator");
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a <= b);

    }

}
