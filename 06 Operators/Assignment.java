public class Assignment {
    public static void main(String[] args) {
        /*
         * # Assignment Operator
         * = - Equal
         * += - Addition
         * -= - Substraction
         * *= - Multiplication
         * /= - Divisio
         * %= - Modulus
         */

        int p = 10;
        int q;

        // assign value using =
        q = p;
        System.out.println(q); // value of q is 10

        // assign value using =+
        q += p;
        System.out.println(q); // value of q is 20
        
        // assign value using =*
        q *= p;
        System.out.println(q); // value of q is 200
    }

}
