public class Arithmetic {
    public static void main(String[] args) {
        /*
         * # Arithmetic Operator
         * +  - Addition
         * -  - Substraction
         * *  - Multiplication
         * /  - Divisio
         * %  - Modulus
         * ++Num - Increment
         * --Num - Decrement
         */
        System.out.println("Arithmetic Operator");

        System.out.println("Addition = "+(10 + 5));
        System.out.println("Substraction = "+(10 - 5));
        System.out.println("Multiplication = "+(10 * 5));
        System.out.println("Division = "+(10 / 5));
        System.out.println("Modulus = "+(10 % 5));
        int num = 5;
        System.out.println("Increment = "+(++num));
        System.out.println("Decrement = "+(--num));

       
        
    }

}
